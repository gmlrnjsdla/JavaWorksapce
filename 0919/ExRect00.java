public class ExRect00 {
	int width;
	int height;
	
	public void setWidth(int width) {
		this.width = width;
	}
	public int getWidth() {
		return this.width;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getHeight() {
		return this.height;
	}
	public void getArea() {
		int area = this.width * this.height;
		System.out.println(area);
	}
}		
	


